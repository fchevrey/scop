#include "libpt.h"

t_4matrix		m4_one(void)
{
	t_4matrix		m;
	int				x;
	int				y;

	x = 0;
	while (x < 4)
	{
		y = 0;
		while (y < 4)
		{
			m.n[x][y] = 1;
			y++;
		}
		x++;
	}
	return (m);
}
