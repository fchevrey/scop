#include "libpt.h"

t_4matrix	m4_scale(t_vecfl factors)
{
	t_4matrix		m;

	m = m4_identity();
	m.n[0][0] = factors.x;
	m.n[1][1] = factors.y;
	m.n[2][2] = factors.z;
	return (m);
}
