#include <stdio.h>
#include "scop.h"

void		m4_print(t_4matrix m)
{
	int				x;

	x = 0;
	while (x < 4)
	{
		printf("%d [ %f %f %f %f ]\n", x, m.n[x][0], m.n[x][1], m.n[x][2], m.n[x][3]);
		x++;
	}
}
